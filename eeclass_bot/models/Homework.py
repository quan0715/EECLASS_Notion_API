class Homework:
    def __init__(self, title, url, course, course_link, deadline, content, id, homework_type,
                 description_content, submission_status, submission_number):
        self.title = title
        self.url = url
        self.course = course
        self.course_link = course_link
        self.deadline = deadline
        self.content = content
        self.id = id
        self.homework_type = homework_type
        self.description_content = description_content
        self.submission_status = submission_status
        self.submission_number = submission_number
