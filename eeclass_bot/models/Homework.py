class Homework:
    def __init__(self, title, url, course, course_link, date, content, type, ID,
                 homework_type, description_content, submission_status, already_submit_number):
        self.title = title
        self.url = url
        self.course = course
        self.course_link = course_link
        self.date = date
        self.content = content
        self.type = type
        self.ID = ID
        self.homework_type = homework_type
        self.description_content = description_content
        self.submission_status = submission_status
        self.already_submit_number = already_submit_number
